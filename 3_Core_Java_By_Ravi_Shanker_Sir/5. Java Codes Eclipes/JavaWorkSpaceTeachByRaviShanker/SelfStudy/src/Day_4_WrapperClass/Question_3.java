package Day_4_WrapperClass;

public class Question_3 {
	public static void main(String[] args) {
		
//		Primitive data type
		System.out.println("Primitive Data Type\n");
		
		byte ob1 = 127;
		System.out.println("Value of 'byte' obj1 = "+ob1);
		
		short ob2 = 128;
		System.out.println("Value of 'short' obj2 = "+ob2);
		
		int ob3 = 25;	
		System.out.println("Value of 'int' obj3 = "+ob3);
		
		long ob4 = 15412L;
		System.out.println("Value of 'long' obj4 = "+ob4);
		
		float ob5 =25F;
		System.out.println("Value of 'float'  obj4 = "+ob5);
		
		double ob6 = 25.0000;
		System.out.println("Value of 'double' obj5 = "+ob6);
		
		char ob7 = 'A';
		System.out.println("Value of 'character' obj7 = "+ob7);
		
		boolean ob8 = true;
		System.out.println("Value of 'boolean' obj7 = "+ob8);
		
		
//		"Java-5.1" we can initialize the  Wrapper Class value directly
		
		System.out.println("\nWrapper Class\n");
		Byte obj1 = 127;
		System.out.println("Value of 'Byte' obj1 = "+obj1);
		
		Short obj2 = 128;
		System.out.println("Value of 'Short' obj2 = "+obj2);
		
		Integer obj3 = 25;	
		System.out.println("Value of 'Integer' obj3 = "+obj3);
		
		Long obj4 = 15412L;
		System.out.println("Value of 'Long' obj4 = "+obj4);
		
		Float obj5 =25F;
		System.out.println("Value of 'Float'  obj4 = "+obj5);
		
		Double obj6 = 25.5555;
		System.out.println("Value of 'Double' obj5 = "+obj6);
		
		Character obj7 = 'A';
		System.out.println("Value of 'Character' obj7 = "+obj7);
		
		Boolean obj8 = true;
		System.out.println("Value of 'Boolean' obj7 = "+obj8);
		
	}
}
